// api/generate-docx.js
// Serverless function — converts plain text content into a properly formatted .docx file
// Uses the `docx` npm package to produce a real Word document (not an HTML blob)

import {
  Document, Packer, Paragraph, TextRun, HeadingLevel,
  AlignmentType, BorderStyle, Table, TableRow, TableCell,
  WidthType, ShadingType, convertInchesToTwip, Header, Footer,
  PageNumber, NumberFormat
} from "docx";

export default async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") return res.status(200).end();
  if (req.method !== "POST") return res.status(405).json({ error: "Method not allowed" });

  const { content, filename = "ProLaunch_Document", type = "default" } = req.body || {};
  if (!content) return res.status(400).json({ error: "Content is required." });

  try {
    const doc = buildDocument(content, type);
    const buffer = await Packer.toBuffer(doc);

    const safeFilename = filename.replace(/[^a-zA-Z0-9_\-]/g, "_");
    res.setHeader("Content-Type", "application/vnd.openxmlformats-officedocument.wordprocessingml.document");
    res.setHeader("Content-Disposition", `attachment; filename="${safeFilename}.docx"`);
    res.setHeader("Content-Length", buffer.length);
    return res.status(200).send(buffer);

  } catch (err) {
    console.error("generate-docx error:", err);
    return res.status(500).json({ error: "Failed to generate document: " + err.message });
  }
}

