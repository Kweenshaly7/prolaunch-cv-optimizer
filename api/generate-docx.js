// ─── Helpers ─────────────────────────────────────────────────────────────────
function coverBlock(title, subtitle) {
  return new Paragraph({
    spacing: { before: 0, after: 160 },
    children: [
      new TextRun({ text: title, bold: true, size: 40, color: FOREST, font: "Calibri", break: 0 }),
      new TextRun({ text: `\n${subtitle}`, size: 22, color: GRAY, font: "Calibri", break: 1 }),
    ],
  });
}

function textToParas(text) {
  return text.split("\n").map(line => {
    if (!line.trim()) return new Paragraph({ spacing: { before: 60, after: 60 }, children: [] });
    if (line.startsWith("•") || line.startsWith("-") || line.trim().startsWith("·")) {
      return new Paragraph({ bullet: { level: 0 }, spacing: { before: 30, after: 30 }, children: [new TextRun({ text: line.replace(/^[•\-·]\s*/, "").trim(), size: 21, font: "Calibri", color: "374151" })] });
    }
    return bodyParagraph(line.trim());
  });
}